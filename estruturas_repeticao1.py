
# Microatividade 2 - Estruturas de Repetição (while)

# Criando uma variável de entrada
entrada_idade = ''

# Laço while para verificar se o valor é diferente de '0'
while entrada_idade != '0':
    entrada_idade = input("Digite um número qualquer ou 0 para sair: ")
    print("Número digitado:", entrada_idade)
